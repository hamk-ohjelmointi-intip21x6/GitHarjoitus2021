namespace GitHarjoitus2021
{
    public class Laskin
    {

        public int Erotus(int luku1, int luku2) {
            return luku1 - luku2;
        }

        public int Summa(int luku1, int luku2)
        {
            return luku1 + luku2;
        }
        
        public int Tulo(int luku1, int luku2) {
            return luku1 * luku2;
        }

    }
}
